#!/usr/bin/env python3
"""
provides a function for finding the floor of a float.
"""
import math


def floor(n: float) -> int:
    """
    finds the floor of a float and returns result as an
    int.

    Parameters:
    n (float): float to find the floor of.

    Returns:
    int: floor of n.
    """
    return math.floor(n)
